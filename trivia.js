let score = 0;
let incorrectAnswers = [];
let attempts = 0;
const maxAttempts = 7;
let shuffledQuestions = []; // מערך לשאלות מעורבות

// פונקציה לערבוב שאלות
function shuffleQuestions(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// פונקציה להתחלת המשחק
function startGame() {
    score = 0;
    incorrectAnswers = [];
    attempts = 0;

    // ערבוב השאלות
    shuffledQuestions = shuffleQuestions([...questions]); // ערבוב השאלות
    showQuestion(0);

    // ניקוי התוצאות הקודמות
    const resultsElement = document.getElementById('results');
    resultsElement.innerHTML = ''; // זה ינקה את התוצאות
}

// פונקציה להציג שאלה
function showQuestion(index) {
    if (index < shuffledQuestions.length && attempts < maxAttempts) {
        const questionElement = document.getElementById('question');
        const answersElement = document.getElementById('answers');

        // הצגת השאלה
        questionElement.innerText = shuffledQuestions[index].question;

        // ערבוב התשובות
        const shuffledAnswers = shuffle([...shuffledQuestions[index].options]);
        answersElement.innerHTML = '';

        shuffledAnswers.forEach((answer, idx) => {
            const button = document.createElement('button');
            button.innerText = answer;
            const isCorrect = shuffledQuestions[index].answer === shuffledAnswers.indexOf(answer); // בדוק אם תשובה זו נכונה
            button.onclick = () => selectAnswer(isCorrect, shuffledQuestions[index].explanation, index);
            answersElement.appendChild(button);
        });
    } else {
        showResults();
    }
}

// פונקציה לבחור תשובה
function selectAnswer(isCorrect, explanation, index) {
    if (isCorrect) {
        score++;
    } else {
        attempts++;
        incorrectAnswers.push({ question: shuffledQuestions[index].question, explanation });
    }
    showQuestion(index + 1);
}

// פונקציה להציג תוצאות
function showResults() {
    const resultsElement = document.getElementById('results');
    resultsElement.innerHTML = `<h2>תוצאות:</h2><p>ניקוד: ${score} מתוך ${shuffledQuestions.length - attempts}</p>`;
    
    if (incorrectAnswers.length > 0) {
        resultsElement.innerHTML += `<h3>תשובות שגויות:</h3>`;
        incorrectAnswers.forEach(answer => {
            resultsElement.innerHTML += `<p>${answer.question} - ${answer.explanation}</p>`;
        });
    }

    // כפתור להתחלה מחדש
    const restartButton = document.createElement('button');
    restartButton.innerText = "התחל מחדש";
    restartButton.onclick = startGame;
    resultsElement.appendChild(restartButton);
}

// פונקציה לערבוב תשובות
function shuffle(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

